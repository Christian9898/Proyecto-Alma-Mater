
package Modelo;

public class alumno {
    private int codalu;
    private String nomalu;
    private String apepaalu;
    private String apemaalu;
    private String idenalu;
    private int telalu;
    private String diralu;
    private String fecnalu;
    private String genalu;
    private String segualu;
    private int nivalu;
    private int graalu;
    private String estado;

    public alumno() {
    }

    public alumno(int codalu, String nomalu, String apepaalu, String apemaalu, String idenalu, int telalu, String diralu, String fecnalu, String genalu, String segualu, int nivalu, int graalu, String estado) {
        this.codalu = codalu;
        this.nomalu = nomalu;
        this.apepaalu = apepaalu;
        this.apemaalu = apemaalu;
        this.idenalu = idenalu;
        this.telalu = telalu;
        this.diralu = diralu;
        this.fecnalu = fecnalu;
        this.genalu = genalu;
        this.segualu = segualu;
        this.nivalu = nivalu;
        this.graalu = graalu;
        this.estado = estado;
    }

    public int getCodalu() {
        return codalu;
    }

    public void setCodalu(int codalu) {
        this.codalu = codalu;
    }

    public String getNomalu() {
        return nomalu;
    }

    public void setNomalu(String nomalu) {
        this.nomalu = nomalu;
    }

    public String getApepaalu() {
        return apepaalu;
    }

    public void setApepaalu(String apepaalu) {
        this.apepaalu = apepaalu;
    }

    public String getApemaalu() {
        return apemaalu;
    }

    public void setApemaalu(String apemaalu) {
        this.apemaalu = apemaalu;
    }

    public String getIdenalu() {
        return idenalu;
    }

    public void setIdenalu(String idenalu) {
        this.idenalu = idenalu;
    }

    public int getTelalu() {
        return telalu;
    }

    public void setTelalu(int telalu) {
        this.telalu = telalu;
    }

    public String getDiralu() {
        return diralu;
    }

    public void setDiralu(String diralu) {
        this.diralu = diralu;
    }

    public String getFecnalu() {
        return fecnalu;
    }

    public void setFecnalu(String fecnalu) {
        this.fecnalu = fecnalu;
    }

    public String getGenalu() {
        return genalu;
    }

    public void setGenalu(String genalu) {
        this.genalu = genalu;
    }

    public String getSegualu() {
        return segualu;
    }

    public void setSegualu(String segualu) {
        this.segualu = segualu;
    }

    public int getNivalu() {
        return nivalu;
    }

    public void setNivalu(int nivalu) {
        this.nivalu = nivalu;
    }

    public int getGraalu() {
        return graalu;
    }

    public void setGraalu(int graalu) {
        this.graalu = graalu;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

}
